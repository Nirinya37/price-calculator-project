print("โปรแกรมคำนวณราคารวมสินค้า")

ราคา1 = float(input("ราคาสินค้าชิ้นที่ 1: "))
จำนวน1 = int(input("จำนวนสินค้าชิ้นที่ 1: "))

ราคา2 = float(input("ราคาสินค้าชิ้นที่ 2: "))
จำนวน2 = int(input("จำนวนสินค้าชิ้นที่ 2: "))

ราคา3 = float(input("ราคาสินค้าชิ้นที่ 3: "))
จำนวน3 = int(input("จำนวนสินค้าชิ้นที่ 3: "))

ราคารวม = (ราคา1 * จำนวน1) + (ราคา2 * จำนวน2) + (ราคา3 * จำนวน3)

print("ราคารวมทั้งหมด =", ราคารวม, "บาท")




